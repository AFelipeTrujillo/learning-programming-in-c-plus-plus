// This is a simple C++ program that prints "Hello, World!" to the console.
// The include directive is used to include the iostream library, which allows us to use input and output streams. 
#include <iostream>

// The main function is the entry point of the program, where we use std::cout to print the message to the console, 
// followed by std::endl to ensure the output is followed by a newline character. Finally, we return 0 to indicate that the program ended successfully.
int main() {
    std::cout << "Hello, World!" << std::endl; 
    return 0;    
}